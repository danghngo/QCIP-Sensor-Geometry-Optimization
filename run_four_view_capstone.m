%% ============================================================
% LOCAL FUNCTIONS
% ============================================================
function [reconOut, statsOut, metricsOut] = run_single_view_reconstruction( ...
    azimuthDeg, elevationDeg, baselineVal, rangeVal, hfovDegVal, imageSizeVal, ...
    disparityRangeVal, maxDepthVal, minValidDispVal, backgroundThresholdVal, ...
    cropXVal, cropYVal, cropZVal, ...
    gtMeshSpacingVal, maxEvalPointsVal, chunkSizeNNVal, chunkSizeMeshVal, triangleCandidatesVal)

    [renderData, rig, sceneInfo] = render_jason_ship_stereo( ...
        'ShipScript', 'oil_tanker_generate_Tanker.m', ...
        'AzimuthDeg', azimuthDeg, ...
        'ElevationDeg', elevationDeg, ...
        'Range', rangeVal, ...
        'Baseline', baselineVal, ...
        'ImageSize', imageSizeVal, ...
        'HFOVDeg', hfovDegVal, ...
        'MakeFigure', false, ...
        'Verbose', false, ...
        'SaveImages', false);

    reconOut = reconstruct_pointcloud(renderData, rig, ...
        'DisparityRange', disparityRangeVal, ...
        'MakeFigure', false, ...
        'Verbose', false, ...
        'SavePointCloud', false, ...
        'MaxDepth', maxDepthVal, ...
        'UseAdaptiveHistEq', true, ...
        'MinValidDisparity', minValidDispVal, ...
        'BackgroundThreshold', backgroundThresholdVal);

    statsOut = analyze_pointcloud(reconOut, ...
        'CropX', cropXVal, ...
        'CropY', cropYVal, ...
        'CropZ', cropZVal, ...
        'MakeFigure', false, ...
        'Verbose', false);

    metricsOut = compute_gt_metrics_jason_ship(reconOut, ...
        'CropX', cropXVal, ...
        'CropY', cropYVal, ...
        'CropZ', cropZVal, ...
        'GTMeshSpacing', gtMeshSpacingVal, ...
        'MaxEvalPoints', maxEvalPointsVal, ...
        'ChunkSizeNN', chunkSizeNNVal, ...
        'ChunkSizeMesh', chunkSizeMeshVal, ...
        'TriangleCandidates', triangleCandidatesVal, ...
        'MakeFigure', false, ...
        'Verbose', false);
end

