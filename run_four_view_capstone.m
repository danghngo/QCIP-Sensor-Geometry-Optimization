function write_case_txt(txtPath, caseLabel, azimuths, elevDeg, baselineVal, rangeVal, hfovDegVal, imageSizeVal, metrics)
    fid = fopen(txtPath, 'w');
    if fid == -1
        return;
    end
    fprintf(fid, 'CaseLabel: %s\n', caseLabel);
    fprintf(fid, 'Azimuths: %s\n', mat2str(azimuths));
    fprintf(fid, 'Elevation_deg: %.6f\n', elevDeg);
    fprintf(fid, 'Baseline: %.6f\n', baselineVal);
    fprintf(fid, 'Range: %.6f\n', rangeVal);
    fprintf(fid, 'HFOV_deg: %.6f\n', hfovDegVal);
    fprintf(fid, 'ImageSize: [%d %d]\n', imageSizeVal(1), imageSizeVal(2));
    fprintf(fid, '\n');
    fprintf(fid, 'Merged mesh RMSE (m): %.12f\n', metrics.mesh_surface_one_sided_rmse);
    fprintf(fid, 'Merged sym centroid RMSE (m): %.12f\n', metrics.centroid_symmetric_rmse);
    fprintf(fid, 'Merged GT area frac @ 0.05 m: %.12f\n', metrics.covered_gt_area_fraction_005);
    fprintf(fid, 'Merged GT area frac @ 0.10 m: %.12f\n', metrics.covered_gt_area_fraction_010);
    fprintf(fid, 'Merged GT area frac @ 0.20 m: %.12f\n', metrics.covered_gt_area_fraction_020);
    fprintf(fid, 'Merged cropped point count: %.0f\n', metrics.cropped_point_count);
    fclose(fid);
end